function jump(k){
	switch(k){
		case 1:
			window.location.href='issue.html';
			break;
		case 2:
			window.location.href='list.html';
			break;
		case 3:
			window.location.href='index.html';
			break;
		case 4:
			window.location.href='edit.html';
			break;
		case 5:
			window.location.href='money.html';
			break;
		case 6:
			$('#repass').popup();
			break;
		case 7:
			window.location.href='login.html';
	}
}